nome = "Lorenzo Bellini"

print (nome[0]) #Vai mostar o primeiro caractere

print (nome[:7]) # Vai mostrar até o sexto caractere

print (nome[6:]) # Vai mostrar a partir do 6 caractere

print (nome[8:14]) # Vai mostrar do oitavo ao décimo quarto caractere

print (nome[8:14:2]) # Vai mostrar do oitavo ao décimo quarto caractere, de 2 em 2

print (nome[:]) # Vai mostrar tudo

print (nome[::-1]) # Vai mostrar tudo, ao contrário