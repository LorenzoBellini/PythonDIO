# Dicionário feito com chaves e valores
pessoa = {"nome": "Lorenzo", "idade": 25}
print(pessoa)

# Dicionário feito com a função dict()
pessoa2 = dict(nome="Lorenzo", idade=25)
print(pessoa2)

# Com o dicionário pessoa2 criado, podemos criar novos valores
pessoa2["sexo"] = "Masculino"
print(pessoa2)
